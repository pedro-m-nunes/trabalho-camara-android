package br.ifsul.quatroi.camaracamera2.auxiliar.exceptions;

public class BottomNavigationMenuException extends Exception {

    public BottomNavigationMenuException() {
    }

    public BottomNavigationMenuException(String message) {
        super(message);
    }

    public BottomNavigationMenuException(String message, Throwable cause) {
        super(message, cause);
    }

    public BottomNavigationMenuException(Throwable cause) {
        super(cause);
    }

    public BottomNavigationMenuException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
